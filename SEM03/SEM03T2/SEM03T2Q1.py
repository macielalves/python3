PI = 3.141592
raio = float(input("Digite o valor do raio: "))
circunferencia = 2 * PI * raio
area_do_circulo = PI * raio ** 2
area_da_esfera = 4 * PI *raio **2
volume_da_esfera = 4/3 * PI * raio ** 3
print("A Circunferêcia é %.6f" % circunferencia)
print("A área do círculo é %.6f" % area_do_circulo)
print("A área da esfera é %.6f" % area_da_esfera)
print("O volume da esfera é %.6f" % volume_da_esfera)


